/**
 * 根据菜品名称和产地描述获取坐标
 */
export function getDishCoords(dishName: string, originText?: string): [number, number] | null {
  // 1. 首先尝试精确匹配菜品名称
  for (const [name, coords] of Object.entries(dishCoords)) {
    if (dishName.includes(name) || name.includes(dishName.trim())) {
      return coords;
    }
  }

  // 2. 如果有产地文本，尝试匹配地区
  if (originText) {
    for (const [region, coords] of Object.entries(regionCoords)) {
      if (originText.includes(region)) {
        return coords;
      }
    }
  }

  // 3. 返回默认值（江南地区 - 随园食单的发源地）
  return null;
}
